package Predicate.SoftwareEngineer;

public class SoftwareEngineer {
    private String name;
    private int age;
    private boolean isHavingTicket;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public boolean isHavingTicket() {
        return isHavingTicket;
    }

    public void setHavingTicket(boolean havingTicket) {
        isHavingTicket = havingTicket;
    }
}
